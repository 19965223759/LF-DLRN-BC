def multi_models_roc(names, colors, y_test_S, y_predict_S, dpin=100):
    """
    Output roc diagrams of multiple machine models to one diagram.

    Args:
        names: list, Names of multiple models
        sampling_methods: list, Instantiated objects of multiple models
        save: Select whether to save the result (default is png format)

    Returns:
        Returns the picture object plt
    """
    plt.figure(figsize=(30, 30), dpi=dpin)
    plt.xticks(fontsize=40)
    plt.yticks(fontsize=40)


    for (name, colorName, y_test, y_predict) in zip(names, colors, y_test_S, y_predict_S):
        fpr, tpr, thresholds = skl.metrics.roc_curve(y_true=y_test, y_score=y_predict, pos_label=1)

        plt.plot(fpr, tpr, lw=3, label='{} (AUC={:.3f})'.format(name, skl.metrics.auc(fpr, tpr)), color=colorName)
        plt.plot([0, 1], [0, 1], 'r--', lw=3, color='grey')
        plt.axis('square')
        plt.xlim([0, 1])
        plt.ylim([0, 1])
        plt.xlabel('1-specificity', fontsize=40)
        plt.ylabel('Sensitivity', fontsize=40)
        plt.title('ROC comparision', fontsize=40)
        plt.legend(loc='lower right', fontsize=40)

    return plt


